console.log('Welcome to the Operators Tutorial');
let x = 10;
let y = 5;
console.log(x+y);
console.log(x*y);
console.log(x**y);
console.log(y%x);

//Increment 
console.log(++x);
console.log(y++);//Increment done post print 
console.log(y);

//Decrement same as Increment

console.log(--x);
console.log(y--);//Increment done post print 
console.log(y);
